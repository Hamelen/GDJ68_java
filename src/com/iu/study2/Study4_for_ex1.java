package com.iu.study2;

public class Study4_for_ex1 {

	public static void main(String[] args) {
		//0~10미만의 숫자 중에서 짝수만을 출력해주세요
		
		for(int i=0; i<10; i+=2) {
//			System.out.printf("%d\t", i);
			System.out.println(i);	
		}
		
		//a-z 출력
		
		for(char i='a'; i<='z'; i++) {      
			System.out.println(i);
		}
		
	}

}
